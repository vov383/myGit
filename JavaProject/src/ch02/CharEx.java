package ch02;

public class CharEx {

	public static void main(String[] args) {
		
		char aValue = '\u0041';
		System.out.println("char형('\u0041') : " + aValue);
		char bValue = '\u0041';
		System.out.println("char형(\'\\u0041\') : " + bValue);//String 안에 unicode 를 표현하기 위해서
		// \' => Single Quote 표현.
		// \\ = Backslash 를 표현.
		 
	
	
	}					// \\ = Backslash 를 표현.
}
